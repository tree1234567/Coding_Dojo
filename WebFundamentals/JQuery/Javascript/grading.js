function grading(grade){
    if (grade >= 98){
        console.log("A+")
    }
    else if (grade >= 93){
        console.log("A")
    }
    else if (grade >= 90){
        console.log("A-")
    }
    else if (grade >= 88){
        console.log("B+")
    }
    else if (grade >= 83){
        console.log("B")
    }
    else if (grade >= 80){
        console.log("B-")
    }
    else if (grade >= 78){
        console.log("C+")
    }
    else if (grade >= 73){
        console.log("C")
    }
    else if (grade >= 70){
        console.log("C-")
    }
    else if (grade >= 60){
        console.log("D")
    }
    else if (grade < 59){
        console.log("F")
    }
}